              <div class="section-content">
                <blockquote class="verma">
                  <div class="uk-h3">Environnement<br></div>
                  <img src="assets/img/c (1).jpg" alt="Image">
                    <div>
                    <span uk-icon="icon: grid" style="color:red"></span> La faune aussi bien que la flore étaient, dans un passé assez 
                    récent, variées, riches en espèces et de tendance à la pluri spécificité, parce que l'équilibre des écosystèmes 
                    existait. Aujourd'hui, la diversité de la faune et de la flore est un lointain souvenir. La tendance est plutôt 
                    la diversification, de plus en plus exacerbée, et de manière auto entretenue par la conjugaison des actions 
                    anthropologiques et climatiques.<br>Les aspects physiques de ce département sont tels que le bassin versant qui 
                    le caractérise provoque les eaux de ruissellement d'une grande ampleur et sans maitrises pendant la saisons des 
                    pluies. Ce qui cause l'érosion hydrique des sols, qui deviennent de plus en plus pauvres. Ce département est ainsi 
                    parcouru par énormément de cours d'eau qui, à leur tours provoquent l'inaccessibilité des villages assis autour des cours d'eau. le problème est d'autant grave à cause du manque d'infrastructures routières <mark><i><strong>(ponts-remblais)</strong></i></mark> permettant la navigabilité des tronçons entre les villages, qui deviennent complétement coupés du reste du monde pendant la saison hivernale.
                    </div>              
                </blockquote>
