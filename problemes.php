                <blockquote class="verma">
                  <div class="uk-h3">Santé<br></div>
                  <img src="assets/img/19.jpg" alt="Image">
                  <div>
                    <span uk-icon="icon: grid"style="color:red"></span> 
                    Le village dispose d'un poste de santé très peu équipé. 
                    La population Vermoise fait face à une faible couverture sanitaire. 
                    On note surtout un manque criard d'une structure de santé de référence. 
                    Cela se traduit par de nombreux morts lors d'évacuation de malades sur des routes cahoteuses,
                    impraticables d'un autre âge. Les soins spécialisés ( spécialistes chirurgicales et médicales)
                    sont disponibles au niveau des hôpitaux de niveau 3 qui sont tous implantés dans la région de 
                    Dakar sauf un à Touba dans la région de Diourbel. La médecine générale, la chirurgie générale et 
                    la la césarienne sont disponibles au niveau des hôpitaux de niveau 2 et 1 qui sont implantés au 
                    niveau des chefs-lieux de régions pour les hôpitaux de niveau 2 et dans certains districts pour les 
                    hôpitaux de niveau 1. Des journées de dons de moustiquaires, de sensibilisation contre des maladies ou 
                    contre l'insalubrité, sont souvent organisés sous la direction de l'infirmier Mr Paul SARR.
                    <br><mark><i><strong>A cette occasion une aide financière, matérielle sera la bienvenue.</strong></i></mark>
                  </div>
                </blockquote>
                <blockquote class="verma">
                    <div class="uk-h3">Education<br></div>
                    <img src="assets/img/im5.jpg" alt="Image">
                    <div>
                    <span uk-icon="icon: grid" style="color:red"></span> Le village dispose d'une école primaire et d'une école 
                    Franco-arabe. Les collégiens et lycéens sont obligés de faire des kilomètres de marche pour aller dans les 
                    villages voisins (Dembancane et Hadoubéré). pour ne pas exposer les enfants à d'éventuels dangers, les parents 
                    optent souvent pour l'abandon. Les filles en sont les victimes. Malgré des efforts pour résoudre les problèmes à 
                    l'accès, ce secteur est confronté des problèmes récurrents, comme le manque chronique d'enseignants. les écoles 
                    souvent construites par des émigrés manquent d'enseignants de qualité car tous fuient de façon frustrante de la zone. 
                    Il faut une création de mécanismes de motivations pour le maintien des enseignants.<br>
                    FEMME ET ENFANTS:<br>- Respect des droits de la femme et de l'enfant;<br>
                    - Accès aux soins et à l'éducation;<br>- Responsabilité de la femme dans la gestion de la famille, du village;
                    <br>- Facilité pour l'emploi;<br>- Droit à la création des richesses;<br>- Renforcement des programmes nutritionnels, 
                    etc.<br>Les activités maraîchaires connaissent un essor, elles sont pratiquées par la composante féminime par le 
                    biais des groupements et associations et celles des jeunes qui exploitent des périmètres individuels.
                    </div>
                </blockquote>
