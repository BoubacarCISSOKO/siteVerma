                  <img src="assets/img/c (2).jpg" alt="Image">
                  <div><span uk-icon="icon: grid" style="color:red"></span> Au niveau  social : une sensibilisation de masse est prévue, prône l’unicité qui est l’un de nos objectifs fars. Favoriser l’épanouissement de la population, l’insertion et la réinsertion mais aussi faciliter à l’accès à l’information et informer la population.</div>
