					<p style="color:black;font-family:Times New Roman;font-size: 20px;text-align: justify;padding-top:15px;padding-bottom:15px">Verma forme avec le village de Dembancane, la commune de Dembancane-Verma (département de Kanel, région de Matam). 
						Le village de verma est limité : à l’Est par Dembancane (3km), à l’Ouest par Hadoubéré (2km), à Nord par le Fleuve Sénégal (la Mauritanie), au Sud par Bokiladji (7km). Avec près de<mark> 1400 habitants </mark>, l'économie du village repose 
						essentiellement sur l'agriculture, l'élévage et les revenus de l'émigration. Le climat, l'environnement, les activités économiques, l'exode rural et l'immigration sont autant de facteurs d'appauvrissement sur lesquels, une étude devrait être faite pour trouver des solutions. 
						Le seul atout majeur de ce village reste <mark>le fleuve, entouré de marigots et de rivières</mark>, mais aussi un vaste étendu de surfaces cultivables et ou irrigable. C'est fort de ces constats, que les jeunes de cette patrie s'activent afin d'accélérer le développement de leur 
						village tout en espérant une implication de toutes les Autorités et ONG qui convergent vers l’émergence et l’emploi des jeunes. Notre objectif est de  bénéficier le maximum d’aides financières, matérielles, morales ou physique afin de servir notre cher Village.  
