					<h1></h1>
					<h2 style="font-family:Times New Roman">HISTOIRE DU VILLAGE</h2>
					<p style="color:black;font-family:Times New Roman;font-size: 20px;text-align: justify;padding-top:15px;padding-bottom:-15px">
					Appartenant administrativement à la commune de Dembancane érigée en 2009, le village de Verma possède une école élémentaire, une case de santé, une école coranique « MADARASSA », un marché et quatre marigots relativement poissonneux. 
					Par le passé, ce village s’est installé à trois endroits différents : le premier endroit fut <b>Dioundiouré</b> en <b>1600</b> l’actuel <b>Orkadièré</b> ensuite il s’installa à <b>Toulély</b> l’actuel <b>Mayel kaadji</b> avant de s’installer définitivement en <b>1650</b> au bord du fleuve Sénégal l’actuel <b>Verma</b>. 
					Les premières familles qui s’y installèrent furent les « <b>SYLLANKOS</b> ou <b>SYLLA</b> » qui sont les <b>SOUMARE</b> et les <b>SY</b>. 
					Le premier chef de village fut <b>SILMAN GAYE SOUMARE</b>. A sa création en <b>1650</b>, le village de Verma va par la suite donner « naissance » à deux villages voisins <b>WOMPOU</b> et <b>DEMBANCANE</b>. 
					Ces villages constituant le trio vont conserver des liens sociaux favorisant la cohésion sociale et les mariages inter- familles. Avec sa diversité culturelle, on y trouve des soninkés majoritairement représentés. 
					A leur côté, se trouvent les pêcheurs appelés les <b>Somono</b>, les <b>Bosso</b>, les <b>Thioubalou</b> connus de leur tradition et leur culture, souvent organisent des journées culturelles à travers la danse et la course des pirogues appelée en Soninké « <b>Fourra ourondé</b> ».
					A cela s’ajoutent les Griots appelés Les <b>Gnaxamala</b> qui par leur talent oratoire assurent le volet communicationnel et organisationnel. La force de ce village repose sur l’agriculture pratiquée tout au long de l’année grâce à l’eau du fleuve. Par ailleurs sa richesse réside dans sa diversité culturelle et ethnique. 
					En effet cette dernière crée des opportunités et favorise la cohésion sociale, la tolérance, la solidarité, l’entraide et l’assistance mutuelle.
